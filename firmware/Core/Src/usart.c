/* USER CODE BEGIN 1 */
static void USART1_Send(uint8_t Tx_Data){
	uint16_t ot_cnt = 0;
    while(LL_USART_IsActiveFlag_TC(USART1) != 1){
      ot_cnt++;
      if(ot_cnt > 7200)
        return;
    }
	  LL_USART_TransmitData8(USART1, Tx_Data);
}

int _write(int fd, char* ch, int len){
  for(int i = 0; i < len; i++){
    USART1_Send(*ch++);
  }
  return len;
}

